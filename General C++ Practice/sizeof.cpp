//Write a program that displays the size of different data types

#include <iostream>
using namespace std;
int main()
{
int a;
char b;
float c;
double d;
bool e;

cout<< "the size of int is "<< sizeof(a) <<endl;
cout<< "the size of char is "<< sizeof(b) <<endl;
cout<< "the size of float is "<< sizeof(c) <<endl;
cout<< "the size of double is" << sizeof(d) <<endl;
cout<< "the size of bool is" << sizeof(e) <<endl;

}
